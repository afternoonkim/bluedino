export function tickerHash(ticker: string): number {
  let h = 2166136261;
  for (let i = 0; i < ticker.length; i++) {
    h ^= ticker.charCodeAt(i);
    h = (h * 16777619) >>> 0;
  }
  return h >>> 0;
}

