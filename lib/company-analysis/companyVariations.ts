/**
 * 결정적 변형: 같은 종목은 항상 같은 변형을 받도록 hash → mod로 결정.
 */
export function pickVariant<T>(arr: T[], ticker: string, salt = 0): T {
  if (arr.length === 0) {
    throw new Error("variant array empty");
  }
  const idx = (tickerHash(ticker) + salt) % arr.length;
  return arr[idx];
}

